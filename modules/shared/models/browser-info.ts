/**
 * Contains the information of the browser
 */

export class BrowserInfo {

    /** Name of the browser, default value `Unknown` */
    name = 'Unknown';
    /** Browser version, default value `Unknown` */
    version = '0';
    /** Operating system where the browser is running, default value `Unknown` */
    operatingSystem = 'Unknown';
    /** Determine whether the device is mobile */
    mobile = false;
  }
  